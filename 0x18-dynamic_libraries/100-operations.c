#include "main.h"

/**
 * add - Takes two integers and returns the sum as the result
 *
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: Always int
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Takes two integers and returns the difference as the result
 *
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: Always int
 */

int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Takes two integers and returns the product as the result
 *
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: Always int
 */

int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - Takes two integers and returns the quotient as the result
 *
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: Always int
 */

int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Takes two integers and returns the remainder as the result
 *
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: Always int
 */

int mod(int a, int b)
{
	return (a % b);
}
